	public int maxLength(String letters)
	{
		int max = 0;
		for(int i=0; i<letters.length(); i++){
			for(int j=i; j<letters.length(); j++) {
				int[] count = new int[26];
				boolean flag = true;
				for(int k=i; k<=j; k++){
					count[letters.charAt(k)-'a']++;
					if(count[letters.charAt(k)-'a'] > 1) 
					{
						flag = false;
						break;
					}
				}
				if(flag) max = Math.max(max, j-i+1);
			}
		}
		return max;
	}
	
